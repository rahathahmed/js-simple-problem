function DollarToTaka(taka){ 
    var dollar = taka * 80.85;
    return dollar ;
}
var TicketPrice = DollarToTaka(100);
console.log("Dhaka To NewYork Ticket Price"+"$",TicketPrice);

function EuroToTaka(taka){ 
    var euro = taka * 102.85;
    return euro ;
}
var TicketPrice = EuroToTaka(100);
console.log("Dhaka To Paris Ticket Price"+"$",TicketPrice);